        </h3>

        <p>
          ВАРВАРА: Ещё как стоит! Я начинаю с малого, конечно. Рассказываю, что воду выключить надо, пока зубы чистишь. Что фантики все в мусорку надо кидать. Что за растениями надо ухаживать, поливать их, что они такие же, как он, живые существа и бить их не надо.
