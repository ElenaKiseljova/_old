        </h3>

        <h4>
          Коронавирусная самоизоляция:
        </h4>

        <p>
          Сегодня, чтобы <a target="_blank" href="#">«сгладить кривую»</a> заболеваемости COVID-19 (то есть замедлить распространение вируса и снизить нагрузку на системы здравоохранения), нам приходится подолгу сидеть дома — целыми неделями и даже месяцами.

        </p>
        <p>
          При этом мы можем следить за ситуацией в режиме реального времени, с помощью специальных онлайн-сервисов — например, <a target="_blank" href="#">карты COVID-19 от Института Хопкинса</a> или <a target="_blank" href="#">индекса самоизоляции от Яндекса</a>.

        </p>

        <h4>
          Климатическая самоизоляция:
        </h4>

        <p>
          Если вовремя не решить проблему климатического кризиса, вероятно, человечеству вновь придётся сидеть на всемирной самоизоляции — только уже масштаба комфортабельной квартиры может оказаться недостаточно: появятся специальные защищённые укрытия или даже целые города «под куполом». При этом такая самоизоляция сможет растянуться на долгие годы и даже десятилетия, став по-настоящему бессрочной.

        </p>
        <p>
          Причины возможной климатический самоизоляции: несовместимая с жизнью жара за пределами кондиционируемых помещений и природные катаклизмы, вызванные всё увеличивающейся концентрацией CO₂ и других парниковых газов в атмосфере Земли. Таким образом, мы будем вынуждены самоизолироваться по двум взаимосвязанным причинам: чтобы избежать неблагоприятного влияния изменившейся окружающей среды, а также для снижения выбросов парниковых газов, напрямую влияющих на климат планеты (как это произошло в этом году, когда люди на самоизоляции стали меньше пользоваться транспортом и потреблять в целом).

        </p>
        <p>
