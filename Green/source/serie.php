        <h3>
          Соблюдаем масочный и пропускной режим
        </h3>

        <h4>
          Коронавирусная самоизоляция:
        </h4>

        <p>
          В этом году неизменные атрибуты случившейся пандемии — медицинские маски, резиновые перчатки, а для кого-то и QR-пропуски для перемещения по городу — стали нашими верными спутниками. Мы быстро к ним приспособились и также быстро от них устали. К сожалению, надолго забывать о средствах защиты не стоит: они ещё могут нам понадобиться (ВОЗ <a target="_blank" href="#">допускает</a> вероятность второй волны коронавируса уже этой осенью) и не раз. А изменение климата только ухудшит ситуацию: в ближайшем будущем на территории России <a target="_blank" href="#">прогнозируется</a> существенное расширение ареалов тех видов, которые распространяют заразные болезни, опасные для человека. Например, на Ямале летом 2016 года из-за аномально тёплой погоды появилась сибирская язва. Уже сегодня на территории России отмечается расширение ареала лихорадки Западного Нила, крымской геморрагической лихорадки, клещевого энцефалита. По мере продолжения нагрева планеты возможно увеличение количества таких случаев — а значит, пандемии грозят повторяться вновь.

        </p>

        <h4>
          Климатическая самоизоляция:
        </h4>

        <p>
          При климатическом кризисе всё может обстоять гораздо серьёзнее. Маски и перчатки, эффективные против вирусов, придётся заменить на более бескомпромиссные средства индивидуальной защиты. К примеру, для выхода на открытый воздух человеку придётся надевать специальный скафандр, поддерживающий приемлемый микроклимат и оптимальный уровень кислорода (из-за высокого содержания CO₂ в атмосфере на улице он снизится), а также защищающий от смертельной жары, ураганов и других катаклизмов. Нет, это не шутка и не преувеличение.

        </p>
        <p>
          Возможно, подобные климатические костюмы будут оснащены технологичной диджитал-панелью с индикатором, который выполняет функцию своеобразного климатического «пропуска», наглядно демонстрируя в реальном времени лимит на выбросы парниковых газов, доступный носителю этого девайса. Индикатор может располагаться на стекле шлема скафандра и быть видимым как снаружи, так и изнутри, чтобы сотрудники климатохранительных органов (люди или роботы) могли быстро вас проверить — и, в случае отсутствия индикатора или превышения лимита на выбросы, оштрафовать. В общем, без этого приспособления нельзя будет буквально и шага сделать за пределами укрытия, в котором вам придётся находиться бóльшую часть своей жизни.

        </p>
        <p>
          Как и в случае с пропускным режимом, введённым в Москве во время пандемии, климатический пропускной режим будущего может быть разделён на пропуски разных типов, в зависимости от конкретных целей. Например, у вас будет «рабочий» пропуск, если ваш вид деятельности связан с выбросами, и пропуск «для личных целей», предназначенный для выбросов, относящихся к личному потреблению. Соответственно, у каждого типа пропуска — свой установленный лимит.

        </p>
        <p>
          Таким образом, в соответствии со строгими нормами режима, мы будем либо продолжать применять старые «грязные» технологии, всё время пытаясь ужаться в отведённый лимит CO₂, либо использовать продвинутые «зелёные» технологии, чтобы исключить выбросы полностью.

