        </p>
        <p>
          Конечно, люди пожилого возраста, в силу ослабленного иммунитета и наличия характерных возрастных заболеваний, будут переносить климатический кризис хуже. Но и молодые люди оказываются не менее уязвимы, ведь они будут жить на этой планете дольше — а значит, потенциально чаще рискуют сталкиваться ситуациями, опасными для жизни и здоровья.

        </p>
        <p>
          Сегодня поколение Z (молодые люди, родившиеся ориентировочно в конце девяностых — начале нулевых) всерьёз тревожится о мировых экологических проблемах, в том числе и изменении климата. Согласно опросу, проведённому организацией Amnesty International в 2019 году, 41% молодых людей считают изменение климата главной проблемой современности.

        </p>
        <p>
