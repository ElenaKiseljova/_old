      <article class="content content--publication">
        <h2>
          Что такое платформа «Зелёный курс для России»?
        </h2>

        <p>
          <a target="_blank" href="https://greenrecovery.ru/">«Зелёный курс для России»</a> — это краудсорсинговая платформа, созданная Greenpeace в партнёрстве с Российским социально-экологическим союзом (РСоЭС) и Climate Action Network (CAN). С её помощью мы сформулируем предложения по выходу из экономического и климатического кризиса совместно с общественными движениями, социально и экологически ответственным бизнесом, ведущими экспертами в области экономики и охраны окружающей среды.
        </p>

        <h3>
          Зачем России Зелёный курс?
        </h3>

        <p>
          В нашей стране борьба с климатическим кризисом пока не является приоритетом. У нас также накоплены хронические экологические проблемы, и отсутствует единый, ориентированный на защиту климата и окружающей среды подход к их решению.  А случившаяся в 2020 году пандемия COVID-19 стала очередным «чёрным лебедем», отвлёкшим внимание общества от экологического кризиса.
        </p>

        <p>
          Но это необязательно должно быть так: мы можем думать о коронакризисе и климатическом кризисе в связке — и, занимаясь восстановлением экономики, решать обе проблемы одновременно. И это уже происходит в других странах мира: так, 17 европейских министров, занимающихся вопросами климата и охраны окружающей среды, <a target="_blank" href="https://www.climatechangenews.com/2020/04/09/european-green-deal-must-central-resilient-recovery-covid-19/">высказались</a>  за то, чтобы принятый ранее Европейский зелёный курс стал рамочной основой устойчивого восстановления экономик в связи с COVID-19. А Комитет Организации экономического сотрудничества и развития (ОЭСР) по политике в области окружающей среды <a target="_blank" href="http://www.mnr.gov.ru/press/news/oesr_uluchshenie_okruzhayushchey_sredy_neobkhodimo_vklyuchit_v_paket_mer_po_vykhodu_iz_globalnogo_kr/">принял решение</a>, что улучшение окружающей среды необходимо включить в пакет мер по выходу из коронакризиса.
        </p>

        <p>
          Однако Россия пока идёт по другому пути: существующий на настоящий момент Общенациональный план действий Правительства России по выходу из кризиса не только не учитывает глобальное изменение климата и международный тренд на низкоуглеродное развитие, но и по факту ослабляет существующее экологическое законодательство.
        </p>

        <p>
          Такая недальновидность замедляет развитие нашей страны и ставит под угрозу безопасное будущее людей. Мы считаем, что в России давно назрела необходимость нового, несырьевого и высокотехнологичного пути развития, и население нашей страны готово к Зелёному курсу (ранее более 100 тыс. человек <a target="_blank" href="https://greenpeace.ru/news/2020/06/29/100-tysjach-rossijan-trebujut-jekologichnogo-vyhoda-iz-krizisa/">поддержали</a>  соответствующую петицию Greenpeace). И сейчас необходимо не только сделать Общенациональный план по выходу из кризиса «зелёным», но и сформулировать путь реальных долгосрочных изменений.
        </p>

        <h3>
          Почему сейчас самое лучшее время для перехода на Зелёный курс?
        </h3>

        <p>
          Для этого есть, как минимум, три причины:
        </p>

        <p>
          1)	Климатический кризис не ждёт, и лучшее время для борьбы с ним — не завтра, а сегодня.
        </p>
        <p>
          2)	Мы видим также, что в обществе назрело желание перемен: для 67% жителей нашей страны изменение климата <a target="_blank" href="https://www.ipsos.com/sites/default/files/ct/news/documents/2020-04/g_earth_day_2020_global_deck_v3_embargoed_until_22.04.20_confidential_rus.pdf">представляется</a> таким же серьёзным кризисом, как и COVID-19, а 60% россиян считают важным в рамках восстановления экономики направить действия правительства на решение проблемы изменения климата. Ранее климатический кризис <a target="_blank" href="levada.ru/2020/01/23/problemy-okruzhayushhej-sredy/">уже вышел</a> на четвёртое место среди угроз, волнующих россиян, и сегодня им обеспокоена треть населения нашей страны (кстати, на первом месте — загрязнение окружающей среды, что схоже по смыслу). Кроме того, недавно Greenpeace <a target="_blank" href="https://greenpeace.ru/news/2020/06/29/100-tysjach-rossijan-trebujut-jekologichnogo-vyhoda-iz-krizisa/">передал</a> в правительство подписи более 100 тысяч человек, призывающих усилить экологический контроль и сделать Общенациональный план по выходу из кризиса зелёным.
        </p>
        <p>
          3)	Экономический спад, вызванный COVID-19, даёт государствам возможность выйти из кризиса по зелёному пути, поддержав технологии с низкими выбросами парниковых газов и укрепив долгосрочную приверженность стабилизации климата в рамках Парижского соглашения. Если же вместо этого пойти по привычному сценарию и поддержать грязные отрасли — значит упустить возможность и застрять в прошлом.
        </p>

        <h3>
          Зачем нам объединяться для создания Зелёного курса?
        </h3>

        <p>
          Переход к Зелёному курсу развития экономики — это масштабный и неизбежный выход из зоны комфорта для нашей страны. Поэтому изменения могут стать реальностью только при наличии широкой общественной поддержки. Объединившись, мы сможем вдохновить страну на выход из зоны комфорта и переход к зелёной модели экономики.
        </p>

        <h3>
          Почему нам всем нужен Зелёный курс?
        </h3>

        <p>
          Последствия изменения климата неизбежны для всех стран мира и в ближайшие годы могут повлечь за собой глобальные перемены во всех сферах жизни человечества. «Зелёный» курс - это возможность сегодня позаботиться о будущем. Любые масштабные изменения всегда становятся неожиданностью и непредвиденной ситуацией. Поэтому так важно иметь четкий план действий по переходу на низкоуглеродную, экологичную модель экономики, чтобы в условиях изменяющегося мира занять место лидера и пройти переход с минимальными потерями.
        </p>

        <h3>
          Как принять участие?
        </h3>

        <p>
          Пройти <a target="_blank" href="https://greenrecovery.ru/mnenie/">опрос</a> на платформе.
        </p>

        <p>
          Так же мы ждём <a target="_blank" href="https://greenrecovery.ru/mnenie/">предложений</a> по «зелёному» выходу из кризиса от некоммерческих организаций и бизнес-ассоциаций до 15 августа 2020 года.

        </p>


