                  Природе и так приходится нелёгко: человек постоянно трансформирует глобальную среду, вырубая леса, эксплуатируя и загрязняя почву, воду и воздух. Климатический кризис только ухудшает ситуацию, и сегодня его <a target="_blank" href="https://ipbes.net/sites/default/files/inline/files/ipbes_global_assessment_report_summary_for_policymakers.pdf">ставят</a> уже на третье место среди причин, из-за которых исчезают виды от действий человека — причём его вклад будет только увеличиваться.

                </p>
                <p class="faq__text">
                  Растения и животные способны адаптироваться, однако у всего есть предел. Сегодня из-за вклада человека изменение климата происходит невероятно быстро по сравнению с аналогичными процессами в прошлом (вызванными естественными причинами), поэтому возможность живых организмов приспособиться может быть ограничена.

