                </p>
                <p class="faq__text">
                  Обильные снегопады, заморозки или температура ниже долгосрочной нормы в конкретный день имеют отношение к погоде, которая обладает большей вариативностью. Такие явления не противоречат общему тренду потепления: напротив, есть версия, что случаи экстремального холода могут быть связаны с таянием арктических льдов.

