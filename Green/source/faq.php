                </p>
                <p class="faq__text">
                  Климатические модели не предсказывают ежедневные погодные значения — они рассчитывают средние значения климата. Это делается с помощью современных климатических моделей и суперкомпьютеров и основано на наилучшем доступном на сегодня научном знании о том, как работает климатическая система Земли и что с ней будет в результате вмешательства человека.

                </p>
