                <h3 class="faq__item-title">
                  5. Лето холодное и дождливое, о каком глобальном потеплении речь?
                </h3>

                <p class="faq__text">
                  Легко спутать текущую погоду с долгосрочными климатическими тенденциями: это примерно, как пытаться понять на пляже, идёт прилив или отлив, наблюдая за отдельными волнами.

                </p>
                <p class="faq__text">
                  Но погода — это состояние атмосферы в данном месте в данном время. А когда мы говорим об изменении климата, речь уже идёт о том, что происходит со среднегодовой температурой планеты — «средней температурой по больнице», а также другими показателями, о которых мы говорили выше.

                </p>
                <p class="faq__text">
                  Естественные повышения и понижения ежедневной температуры затрудняют распознавание медленных изменений климата. Но если несколько дней за окном холодно, это не значит, что глобального потепления нет: для того, чтобы узнать, что происходит с климатом, нужно взять определённые показатели за более длительный период. Всемирная метеорологическая организация рекомендует смотреть изменения, как минимум, за 30 лет. Поэтому в научном докладе, описывающем, что происходит с климатом, вы всегда найдёте усреднённые показатели за длительный срок.

                </p>
                <p class="faq__text">
