                <h3 class="faq__item-title">
                  4. Что значит «глобальное потепление на 1,5 °C» и почему нужно стремиться к этой цели?
                </h3>

                <p class="faq__text">
                  Для того чтобы понять, насколько потеплела планета, нужно соблюсти два условия:  прежде всего решить, с какого времени начать отсчёт. Так как изменение климата происходит в основном из-за выбросов парниковых газов в результате деятельности человека (энергетика, промышленные процессы, лесное и сельское хозяйство и т.д.), то за такую точку отсчёта берут среднегодовую температуру планеты в так называемый доиндустриальный период — то есть до того, как мы начали активно промышленно развиваться и нагревать планету.

                </p>
                <p class="faq__text">
                  Второе условие — наличие достоверных данных о температуре. Проблема заключается в том, что по мере того, как мы уходим назад в прошлое,  точных данных всё меньше и сравнивать температуру сложнее. Поэтому Межправительственная группа экспертов ООН по изменению климата <a target="_blank" href="https://www.ipcc.ch/site/assets/uploads/sites/2/2018/12/SR15_FAQ_Low_Res.pdf">использует</a> период с 1850 по 1900 годы как точку отсчёта.

